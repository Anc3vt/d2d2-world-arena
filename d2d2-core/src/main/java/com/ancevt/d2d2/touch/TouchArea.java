
package com.ancevt.d2d2.touch;

class TouchArea {

    private int x;
    private int y;
    private int width;
    private int height;

    TouchArea(final int x, final int y, final int width, final int height) {
        setUp(x, y, width, height);
    }

    public final void setUp(final int x, final int y, final int width, final int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
