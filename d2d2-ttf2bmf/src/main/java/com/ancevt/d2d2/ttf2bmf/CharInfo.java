
package com.ancevt.d2d2.ttf2bmf;

public class CharInfo {
	public char character;
	public int x;
	public int y;
	public int width;
	public int height;

	@Override
	public String toString() {
		return "CharInfo{" +
				"character=" + character +
				", x=" + x +
				", y=" + y +
				", width=" + width +
				", height=" + height +
				'}';
	}
}
